public class PlaceMan{
  public static void Mann2(int i){
    if (i==0){
        System.out.println("   0  *");
      }
      else if (i==1){
        System.out.println("  /|\\ *");
      }
      else if (i==2){
        System.out.println("   |  *");
      }
      else if (i==3){
        System.out.println("  / \\ *");
      }
      else{
        System.out.println("* * * *");
      }
  }
}